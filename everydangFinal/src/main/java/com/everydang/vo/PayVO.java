package com.everydang.vo;

public class PayVO {
	private String py_no;
	private String pr_code;
	private String mm_id;
	private String py_method;
	private String py_date;

	public String getPy_no() {
		return py_no;
	}

	public void setPy_no(String py_no) {
		this.py_no = py_no;
	}

	public String getPr_code() {
		return pr_code;
	}

	public void setPr_code(String pr_code) {
		this.pr_code = pr_code;
	}

	public String getMm_id() {
		return mm_id;
	}

	public void setMm_id(String mm_id) {
		this.mm_id = mm_id;
	}

	public String getPy_method() {
		return py_method;
	}

	public void setPy_method(String py_method) {
		this.py_method = py_method;
	}

	public String getPy_date() {
		return py_date;
	}

	public void setPy_date(String py_date) {
		this.py_date = py_date;
	}

}
