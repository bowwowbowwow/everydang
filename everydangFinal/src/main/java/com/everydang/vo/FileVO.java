package com.everydang.vo;

public class FileVO {
	private String fl_code;
	private String fl_originname;
	private String fl_savename;
	private int fl_size;
	private String fl_upload_date;
	private String fl_uploader;

	public String getFl_code() {
		return fl_code;
	}

	public void setFl_code(String fl_code) {
		this.fl_code = fl_code;
	}

	public String getFl_originname() {
		return fl_originname;
	}

	public void setFl_originname(String fl_originname) {
		this.fl_originname = fl_originname;
	}

	public String getFl_savename() {
		return fl_savename;
	}

	public void setFl_savename(String fl_savename) {
		this.fl_savename = fl_savename;
	}

	public int getFl_size() {
		return fl_size;
	}

	public void setFl_size(int fl_size) {
		this.fl_size = fl_size;
	}

	public String getFl_upload_date() {
		return fl_upload_date;
	}

	public void setFl_upload_date(String fl_upload_date) {
		this.fl_upload_date = fl_upload_date;
	}

	public String getFl_uploader() {
		return fl_uploader;
	}

	public void setFl_uploader(String fl_uploader) {
		this.fl_uploader = fl_uploader;
	}

}
